package com.a7a7.module.codegroup;

public class CodeGroupDto {

	private String seq;
	private String cgName;
	private Integer cgUseNy;
	private Integer cgDelNy;
	private String cgRegDateTime;
	private String cgModDateTime;
	
	public String getSeq() {
		return seq;
	}
	public void setSeq(String seq) {
		this.seq = seq;
	}
	public String getCgName() {
		return cgName;
	}
	public void setCgName(String cgName) {
		this.cgName = cgName;
	}
	public Integer getCgUseNy() {
		return cgUseNy;
	}
	public void setCgUseNy(Integer cgUseNy) {
		this.cgUseNy = cgUseNy;
	}
	public Integer getCgDelNy() {
		return cgDelNy;
	}
	public void setCgDelNy(Integer cgDelNy) {
		this.cgDelNy = cgDelNy;
	}
	public String getCgRegDateTime() {
		return cgRegDateTime;
	}
	public void setCgRegDateTime(String cgRegDateTime) {
		this.cgRegDateTime = cgRegDateTime;
	}
	public String getCgModDateTime() {
		return cgModDateTime;
	}
	public void setCgModDateTime(String cgModDateTime) {
		this.cgModDateTime = cgModDateTime;
	}
	
}
