package com.a7a7.module.code;

public class CodeDto {

	private String seq;
	private String cdName;
	private Integer cdUseNy;
	private Integer cdDelNy;
	private String cdRegDateTime;
	private String cdModDateTime;
	private Integer codeGroup_seq;
	
	
	public String getSeq() {
		return seq;
	}
	public void setSeq(String seq) {
		this.seq = seq;
	}
	public String getCdName() {
		return cdName;
	}
	public void setCdName(String cdName) {
		this.cdName = cdName;
	}
	public Integer getCdUseNy() {
		return cdUseNy;
	}
	public void setCdUseNy(Integer cdUseNy) {
		this.cdUseNy = cdUseNy;
	}
	public Integer getCdDelNy() {
		return cdDelNy;
	}
	public void setCdDelNy(Integer cdDelNy) {
		this.cdDelNy = cdDelNy;
	}
	public String getCdRegDateTime() {
		return cdRegDateTime;
	}
	public void setCdRegDateTime(String cdRegDateTime) {
		this.cdRegDateTime = cdRegDateTime;
	}
	public String getCdModDateTime() {
		return cdModDateTime;
	}
	public void setCdModDateTime(String cdModDateTime) {
		this.cdModDateTime = cdModDateTime;
	}
	public Integer getCodeGroup_seq() {
		return codeGroup_seq;
	}
	public void setCodeGroup_seq(Integer codeGroup_seq) {
		this.codeGroup_seq = codeGroup_seq;
	}
	
}
